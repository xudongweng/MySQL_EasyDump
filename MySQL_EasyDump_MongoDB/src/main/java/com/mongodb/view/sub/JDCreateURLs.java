    private void jbOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOKActionPerformed
        if(!this.jtfLinkName.getText().equals("")){
            if(URLDic.getURL(this.jtfLinkName.getText())==null){
                System.out.println(PublicProperty.getBakid());
                PublicProperty.setBakid(PublicProperty.getBakid()+1);//增加最大备份顺序号
                URL url=new URL(this.jtfUser.getText(), String.valueOf(this.jpfPassword.getPassword()),this.jtfServer.getText(),this.jtfPort.getText(),this.jtfSavePath.getText(),String.valueOf(PublicProperty.getBakid()));
                if(MongoDBController.addLinkname(this.jtfLinkName.getText(), url)==-1){//在MongoDB中添加
                     JOptionPane.showMessageDialog(null, "Add link is error", "Error", JOptionPane.ERROR_MESSAGE);
                }
                this.linkname=this.jtfLinkName.getText();
                URLDic.setURL(this.linkname, url);//添加URL
                this.dispose();
            }else{
                URLDic.printURLs();
                //JOptionPane.showMessageDialog(null, URLDic.getURL(this.jtfLinkName.getText()), "错误", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(null, "Link is not exist", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Link is invaild", "Error", JOptionPane.ERROR_MESSAGE);
            this.jtfLinkName.requestFocus();
        }
    }//GEN-LAST:event_jbOKActionPerformed

    

